package Day1;

public class Conditionals {
    public static void main(String[] args){

        //if condition

        int a = 10;
        if (a == 10) {
            System.out.println("If condition executed");
        }

        //while loop
        int b = 1;

        while(b != 5){
            System.out.println(b);
            b++;
        }

        //for loop

        for(int count = 1; count !=5; count++){
            System.out.println(count);
        }
    }
}
